# declare a variable with a string in it
text = "Remarkable bird, the Norwegian Blue"

# output sliced string - a substring, starting at character at position 11th and ending on 14th (EXCLUDING the 14th)
print(text[11:14])  # bir

# same as above but with a DEFAULT start position
print(text[:14])  # Remarkable bir

# print a string slice from the -7th portion to the last but one position
print(text[-7:-1])  # an Blu

# same as above but with a DEFAULT end position
print(text[-7:])  # an Blue
